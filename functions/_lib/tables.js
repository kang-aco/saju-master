export function getGongmang(ganjji) {
  const idx = GANJJI_INDEX[ganjji] ?? -1;
  if (idx < 0) return ['?','?'];
  const sunStartIdx = Math.floor(idx / 10) * 10;
  const sunStart = GANJJI_60[sunStartIdx];
  return GONGMANG[sunStart] ?? ['?','?'];
}

